# For your reference:
#
# SinglyLinkedListNode:
#     int data
#     SinglyLinkedListNode next
#
#
def has_cycle(head):
    if not head:
        return 0

    curr = head

    while curr:
        if curr.data == 10 ** 6:
            return 1
        else:
            curr.data = 10 ** 6
            curr = curr.next
    return 0
